        /// Nothing is on the cell.
        /// </summary>
        None = 0,
        
        /// <summary>
        /// Red is the colour of the cell.
        /// </summary>
        Red = 1,

        /// <summary>
        /// Green is the colour of the cell.
        /// </summary>
        Green = 2,

        /// <summary>
        /// Blue is the colour of the cell.
        /// </summary>
        Blue = 3,

        /// <summary>
        /// Yellow is the colour of the cell.
        /// </summary>
        Yellow = 4,

        /// <summary>
        /// White is the colour of the cell.
        /// </summary>
        White = 5,

        /// <summary>
        /// Black is the colour of the cell.
        /// </summary>
        Black = 6,

        /// <summary>
        /// Pink is the colour of the cell.
        /// </summary>
        Pink = 7,

        /// <summary>
        /// Orange is the colour of the cell.
        /// </summary>
        Orange = 8,

        /// <summary>
        /// Pink is the colour of the cell.
        /// </summary>
        Purple = 9, 

        /// <summary>
        /// Brown is the colour of the cell.
        /// </summary>
        Brown = 10,

        /// <summary>
        /// Lime is the colour of the cell.
        /// </summary>
        Lime = 11,

        /// <summary>
        /// Grey is the colour of the cell.
        /// </summary>
        Grey = 12,
    }
}
