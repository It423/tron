namespace Tron
{
    /// <summary>
    /// An enumeration to represent the possible directions a car can travel.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// The car is moving up.
        /// </summary>
        Up = 0,

        /// <summary>
        /// The car is moving right.
        /// </summary>
        Right = 1,

        /// <summary>
        /// The car is moving down.
        /// </summary>
        Down = 2,

        /// <summary>
        /// The car is moving left.
        /// </summary>
        Left = 3
    }
}
